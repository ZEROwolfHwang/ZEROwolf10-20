    public static Observable<String> getCodeFromFile(final File mFile, final File mTempFile) {
        return Observable.just(mFile)
                .map(new Func1<File, String>() {
                    BufferedWriter mWriter;
                    BufferedReader mRead;

                    @Override
                    public String call(File file) {
                        String code = null;
                        if (!file.exists())
                            return code;
                        if (mTempFile.exists()) {
                            mTempFile.delete();
                        }
                        try {
                            mTempFile.createNewFile();
                            mRead = new BufferedReader(new InputStreamReader(new FileInputStream(mFile)));
                            mWriter = new BufferedWriter(new OutputStreamWriter(new FileOutputStream(mTempFile, true)));
                            String s1;
                            String firstNum = mRead.readLine();//读取第一行的计数信息
                            String firstNumBefore = firstNum.substring(0, 10);
                            String numCount = firstNum.substring(10, firstNum.length());
                            mWriter.write(firstNumBefore + (Integer.parseInt(numCount) - 1));
                            mWriter.newLine();
                            code = mRead.readLine();
                            while ((s1 = mRead.readLine()) != null && code != null) {
                                mWriter.write(s1);
                                mWriter.newLine();
                            }
                            mWriter.flush();
                        } catch (Exception e) {
                            e.printStackTrace();
                        } finally {
                            try {
                                if (mWriter != null)
                                    mWriter.close();
                                if (mRead != null)
                                    mRead.close();
                            } catch (IOException e) {
                                e.printStackTrace();
                            }
                            return code;
                        }
                    }

                })
                .subscribeOn(Schedulers.io())
                .observeOn(AndroidSchedulers.mainThread());
    }

